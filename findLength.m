%***************************************
% Find length using cosine law
% Find c2
%***************************************
% Input variables
b1 = input("b1 = "); % b1 = 180
b2 = input("b2 = "); % b2 = 165
c1 = input("c1 = "); % c1 = 115 
A1 = input("A1 = "); % A1 = 120
A2 = input("A2 = "); % A2 = 100
%***************************************
% Find a
% Cosine rule: a^2 = b^2 + c^2 - 2 * b * c * cos(A)
a1 = sqrt( (b1^2) + (c1^2) - 2 * b1 * c1 * cosd(A1)); % 257.54
a = a1^2; % a = 66325
% Find c2
c2 = roots([1, -2 * b2 * cosd(A2), b2^2 - a]); % c2 = -228.44, c2 = 171.15
c2 = c2(c2 > 0); % c2 = 171.15
%***************************************
% Display result
disp(['c2 = ', num2str(c2)]);
